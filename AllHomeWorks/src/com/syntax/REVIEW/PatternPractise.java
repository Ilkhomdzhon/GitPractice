package com.syntax.REVIEW;

public class PatternPractise {
	
	
	public static void main(String[] args) {
		
		
		for (int i=1; i<=6;i++) {
			for (int y=1;y<=i;y++) {
				System.out.print("*"+"");
			}
			System.out.println();
		}
			 for (int a=1;a<=6;a++) {
				 for (int b=1;b<=a;b--) {
					 System.out.print("*"+"");
				 }
				 System.out.println();
				 
			 }
		
	}

}
